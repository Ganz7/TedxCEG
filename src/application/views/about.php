			<div class="row" id="ted">
				<p class="red"><b class="large">TED<span class="grey"><sup>x</sup>CEG</span></b></p>
				<p>TED is an assortment of conferences conducted on an international level by the non-profit organization Sapling Foundation with the motto of <i>“Ideas worth spreading”</i>. The first conference held in 1984, was initially based on ideas on technology and design but over the years, it has expanded to a variety of fields such as science, philosphy, music, relgion etc. The international TED conference is conducted on an annual basis where the world’s pioneers in technological innovation speak for a period of 18 minutes. The TED prize is an annual prize  of 1 million dollars awarded to a single winner and the winner gets to realize  his “ wish to change the world” which will be implemented by TED.</p>
			</div>
			<div class="row" id="tedx">
				<p class="red"><b class="large">TED<sup>x</sup><span class="grey">CEG</span></b></p>
				<p>True to the mission of TED, TEDX, comprising of independently organised TED events, is aimed at sparking discussions and encouraging extensive learning. Achievers from different streams render talks on various streams from technology to entertainment. Apart from the live speakers, TEDx events also include TED videos and demonstrations to provide the perfect TED-like experience to the attendees. The TEDx conferences are self-organized and replicate the TED conference in their respective organizations.</p>
			</div>
			<div class="row" id="tedxceg">
				<p class="red"><b class="large">TED<sup>x</sup>CEG</b></p>
				<p>TEDxCEG was the brainchild of Dr.Madhan Karky, popularly known as 'the technical lyricist' and also a former faculty of CEG, in the year 2012. It was started with the vision of creating a knowledge platform at the varsity level where prominent speakers share ideas that change lives across the world." Embracing technology" was the theme of the first conference which highlighted innovations in technology that have revolutionized the lives of the common man throughout the world in a sustainable manner.</p>
